package org.nathan.AlgorithmsJava.tools;

public class Quaternion<I1, I2, I3, I4> {
    public final I1 first;
    public final I2 second;
    public final I3 third;
    public final I4 fourth;

    public Quaternion(I1 a, I2 b, I3 c, I4 d) {
        first = a;
        second = b;
        third = c;
        fourth = d;
    }
}
