package Algorithms.tools;

public final class SimpleDate {
    public int year;
    public int month;
    public int day;

    public SimpleDate(int y, int m, int d) {
        this.year = y;
        this.month = m;
        this.day = d;
    }

    public int getYear() {
        return year;
    }

    public int getMonth() {
        return month;
    }

    public int getDay() {
        return day;
    }
}
